<font size=4><p> Destinations aux Etats Unis<br />
 Depuis des années, les touristes choisissent pour de bonnes raisons quelques villes <br />
comme leurs principales destinations aux États-Unis. Tanner Latham est l\'ancien <br />
responsable de la section voyage du magazine Southern Living. Chaque année, la <br />
plupart des touristes étrangers (à l\'exception des Canadiens et des Mexicains) qui <br />
viennent aux États-Unis passent la plus grande partie de leur séjour dans quelques <br />
villes, dont on trouvera ci-dessous la liste en ordre décroissant de popularité. <br />
Certaines de ces villes sont recherchées à cause de leur tourbillon d\'activité, <br />
tandis que d\'autres le sont pour leur calme.<br />
    <br />
New York <br />
New York est célèbre pour sa Bourse des valeurs située dans Wall Street, le quartier <br />
des théâtres, le gratte-ciel Empire State, la statue de la Liberté, des musées <br />
renommés et des marchands ambulants qui offrent des mets de divers pays. Cette ville <br />
retient l\'attention du monde. Tout grand cuisinier qui y ouvre un restaurant, tout <br />
styliste qui y présente une collection de vêtements et tout artiste qui expose ses <br />
œuvres dans une galerie ressentent la grande énergie qui se dégage de tous les <br />
quartiers de ce qu\'on appelle la Grande Pomme. Les touristes ont le plaisir <br />
d\'absorber cette énergie, qu\'ils soient touchés par le tohu-bohu de Times Square ou <br />
par la présentation moins tapageuse des artistes qui vendent leurs œuvres dans <br />
l\'Union Square.<br />
<br />
Los Angeles<br />
Los Angeles est surtout connue pour deux de ses quartiers qui sont très célèbres. <br />
Hollywood offre le faste, l\'éclat et des visions fugitives de la célébrité. C\'est là <br />
où l\'on trouve les plus grandes vedettes de cinéma du monde. Les touristes peuvent <br />
les apercevoir brièvement lorsqu\'elles descendent Rodeo Drive et le boulevard Sunset <br />
ou qu\'elles posent devant le Théâtre chinois de Grauman. Le quartier de Venice Beach <br />
est tout à fait différent. Le style de vie y est décontracté. Sa promenade au bord de <br />
l\'océan, qui attire des gens de tous les milieux, est l\'un des meilleurs endroits <br />
pour observer des personnes de tout acabit.<br />
<br />
San Francisco<br />
San Francisco est une ville influencée par la brise soufflant de l\'océan Pacifique. <br />
Le temps qu\'il fait dicte toutes les décisions prises aussi bien par ses habitants <br />
que par les touristes. Le parc de Golden Gate, doté de nombreux chemins, de plages et <br />
de vues sur le célèbre pont Golden Gate, attire surtout les visiteurs lorsque le <br />
soleil brille, même si l\'air est un peu frais. Les jours de brouillard, il est bon de <br />
s\'aventurer dans le centre-ville, de prendre le funiculaire ou de se promener dans <br />
les diverses collines de la ville où les rues très en pente sont bordées de maisons <br />
de style victorien ou édouardien souvent photographiées ou filmées.<br />
<br />
Miami et sa plage<br />
Comme un air de danse endiablée, Miami et Miami Beach connaissent une activité <br />
incessante, quelle que soit l\'heure de la journée. South Beach attire quelques-unes <br />
des personnes les plus belles et compte même une série de postes de sauvetage de <br />
style art déco. La nuit, les néons brillants de couleur rouge, verte, bleu et rose <br />
transforment l\'avenue Ocean Drive en un lieu chic que l\'on ne trouve nulle part <br />
ailleurs. L\'influence cubaine se fait sentir partout, tant dans les restaurants que <br />
dans les quartiers comme Little Havana où la rue Calle Ocho (8e rue) constitue un <br />
lieu de réunion. Les hommes y viennent pour échanger des plaisanteries en espagnol <br />
tout en fumant le cigare et en jouant aux dominos.<br />
<br />
Orlando<br />
Le grand Orlando est le lieu même de l\'évasion. C\'est là où on peut laisser courir <br />
son imagination. Même les touristes les plus imaginatifs ne peuvent pas inventer les <br />
mondes créés au parc Universal et à Disney World ou dans tout autre centre de <br />
divertissement de Disney. Orlando n\'est pas seulement un lieu de rêves, de fantaisie <br />
et de personnages revêtus de costumes divers. Le centre-ville offre aux visiteurs un <br />
mélange architectural intéressant, allant de maisons de style victorien à des <br />
immeubles de grande hauteur. Il comprend aussi un grand nombre de boutiques, de <br />
restaurants et de boîtes de nuit. Pour ceux qui aiment la nature, le parc du lac Eola <br />
offre aux piétons une belle promenade.<br />
<br />
Las Vegas<br />
out Las Vegas est une fête. Il faut imaginer un grand centre d\'attraction pour <br />
adultes dont l\'éclairage est à giorno et les bâtiments extravagants. Il suffit de se <br />
promener le long de ce que l\'on appelle le Las Vegas Strip pour s\'en rendre compte. <br />
On y trouve d\'immenses fontaines, des jeux de lumière laser, des casinos où les <br />
enjeux sont gros et des spectacles de grande qualité. Dormir est le dernier des <br />
soucis de quiconque se rend à Las Vegas. Les touristes à la recherche d\'une vie <br />
relativement plus calme peuvent aller dans le quartier de la rue Freemont où la <br />
plupart des casinos sont plus vieux jeu.<br />
<br />
Honolulu<br />
Si la ville d\'Honolulu offre de nombreux magasins et musées et le jardin botanique <br />
Foster, elle constitue surtout un point de départ pour la visite de l\'île d\'Oahu. La <br />
plupart des touristes restent à Waikiki qui est doté d\'excellentes plages, d\'un grand <br />
nombre de magasins, de restaurants et qui offre une multitude d\'activités de plein <br />
air, dont le surf et la voile. Pour ce qui est du surf, la plupart des habitants de <br />
l\'île et des touristes pratiquent ce sport à Haleiwa, petite ville située au Nord. <br />
Les personnes qui s\'intéressent à l\'histoire navale des États-Unis peuvent visiter le <br />
navire Arizona qui constitue un monument en l\'honneur de ceux qui ont péri lors de <br />
l\'attaque contre Pearl Harbor en décembre 1941, ainsi que l\'ancien navire de guerre <br />
Missouri.<br />
<br />
Washinton<br />
Washington incarne l\'esprit des États-Unis. C\'est une ville riche en histoire, dont <br />
les Américains sont très fiers et où la vie politique est incessante. Elle abrite <br />
quelques-uns des plus grands musées du pays, notamment le Musée national de l\'air et <br />
de l\'espace et la Galerie nationale d\'art. Elle a su conserver ses espaces verts et <br />
on est surpris de trouver la solitude en se promenant dans l\'île Roosevelt située au <br />
milieu du Potomac. Bien entendu, l\'un des sites qui attirent le plus les touristes <br />
est la vaste esplanade du National Mall. On peut s\'asseoir sur l\'herbe et se reposer <br />
tout en étant entouré de monuments érigés en l\'honneur de quelques-uns des grands <br />
présidents du pays.<br />
<br />
Chicago<br />
Connue pour ses équipes de sport professionnel, ses restaurants et son architecture, <br />
Chicago est la ville la plus importante du centre des États-Unis. De tous les surnoms <br />
qu\'on lui donne, celui de la « ville des quartiers » semble le mieux adapté. Au <br />
nombre de plus de soixante-dix, Ils ont chacun leur propre caractère. La ville, qui <br />
longe le lac Michigan, offre aussi aux touristes de faire du lèche-vitrine et des <br />
achats, en particulier dans les boutiques et les magasins de haute couture situés le <br />
long de l\'avenue Michigan surnommée le « Magnificent Mile ».<br />
<br />
Boston<br />
Boston est vraiment la perle de la Nouvelle-Angleterre. L\'une des meilleures façons <br />
pour les touristes de faire connaissance avec la ville est de se promener dans ses <br />
parcs dont le Boston Common et le Public Garden où des barques en forme de cygne <br />
sillonnent une vaste étendue d\'eau depuis les années 1870. La ville met en valeur le <br />
rôle essentiel qu\'elle a joué dans l\'histoire du pays, mais ne se concentre jamais <br />
sur le passé. Par exemple, Faneuil Hall, où de grands orateurs ont manifesté leurs <br />
protestations avant la guerre d\'Indépendance, abrite maintenant de nombreux magasins <br />
et restaurants dans une zone pédestre. Le port de Boston, qui fut le lieu de révolte <br />
contre la taxation du thé par les Anglais au XVIIIe siècle, est maintenant doté d\'une <br />
promenade le long de l\'eau.<br />
